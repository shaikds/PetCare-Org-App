import 'package:cloud_firestore/cloud_firestore.dart';

class Pet {
  late String
      _uid; // Firestore document ID (uid is auto-generated by Firestore)
  late String _name;
  late String _gender;
  late int _age;
  late String _size;
  late int _energyLevel;
  late String _description;
  late List<String> _photos = new List<String>.empty(growable: true);
  late bool _isAdopted;
  late int descriptionScore=0;

  //Constructor

  Pet({
    required String name,
    required String gender,
    required int age,
    required String description,
    required int energyLevel,
    required isAdopted,
    required String size,
    required List<String> photos,
  })  : _isAdopted = false,
        _description = description,
        _age = age,
        _gender = gender,
        _name = name,
        _energyLevel = energyLevel,
        _size = size,
        _photos = photos;


  //gettters and setters

  String get uid => _uid;

  set uid(String value) {
    _uid = value;
  }

  String get size => _size;

  set size(String value) {
    _size = value;
  }

  int get energyLevel => _energyLevel;

  set energyLevel(int value) {
    _energyLevel = value;
  }

  String get name => _name;

  set name(String value) {
    _name = value;
  }

  String get gender => _gender;

  set gender(String value) {
    _gender = value;
  }

  int get age => _age;

  set age(int value) {
    _age = value;
  }

  String get description => _description;

  set description(String value) {
    _description = value;
  }

  List<String> get photos => _photos;

  set photos(List<String> value) {
    _photos = value;
  }

  bool get isAdopted => _isAdopted;

  set isAdopted(bool value) {
    _isAdopted = value;
  }

  //Other methods
  // Convert the Pet object to a Map for saving in Firestore
  Map<String, dynamic> toJson() {
    return {

      'name': _name,
      'gender': _gender,
      'age': _age,
      'description': _description,
      'photos': _photos,
      'energyLevel': _energyLevel,
      'isAdopted': _isAdopted,
      'size':   _size,
    };
  }

  // Create a Pet object from a Firestore document snapshot
  factory Pet.fromJson(DocumentSnapshot snapshot) {
    //TODO : Change here?
    Map<String, dynamic>? data = snapshot.data() as Map<String, dynamic>?;
    if (data == null) {
      throw Exception("Invalid snapshot data for Pet");
    }
    Pet pet = Pet(
        name: data['name'],
        gender: data['gender'],
        age: data['age'],
        size: data['size'],
        description: data['description'],
        isAdopted: data['isAdopted'] ?? false,
        photos: List<String>.from(data['photos'] ?? []),
        energyLevel: data['energyLevel']);
        pet.uid= snapshot.id;

    return pet;
  }

  @override
  String toString() {
    return 'Pet{_name: $_name, _gender: $_gender, _age: $_age, _energyLevel: $_energyLevel, _description: $_description}\n';
  }
}
